num1 = int(input("请输入第一个数字："))
num2 = int(input("请输入第二个数字："))

print(num1 < num2)
print(num1 <= num2)
print(num1 > num2)
print(num1 >= num2)
print(num1 == num2)
print(num1 != num2)


# 两个等于号,判断的是值和数类型同时相等
print('5' == 5)
print(3 == 3)
print('3' == '3')