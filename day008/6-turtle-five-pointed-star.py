import turtle

turtle.begin_fill()
turtle.color('gold')

# 沿着箭头方向前进200
turtle.forward(200)
# 箭头向右转144度
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.end_fill()
turtle.hideturtle()

turtle.done()








